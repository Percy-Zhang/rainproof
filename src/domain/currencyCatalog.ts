type IntlWithCurrencySupport = typeof Intl & {
  supportedValuesOf?: (key: 'currency') => string[];
};

type IntlWithDisplayNames = typeof Intl & {
  DisplayNames?: new (
    locales?: string | string[],
    options?: { type: 'currency' },
  ) => { of: (code: string) => string | undefined };
};

export type CurrencyOption = {
  code: CurrencyCode;
  label: string;
  symbol: string;
};

export type CurrencySearchOption = CurrencyOption & {
  searchText: string;
};

export function getAvailableCurrencyCodes(): CurrencyCode[] {
  const supportedValuesOf = (Intl as IntlWithCurrencySupport).supportedValuesOf;
  const runtimeCodes = supportedValuesOf?.('currency') ?? [];
  const codes = runtimeCodes.length ? runtimeCodes : [...FALLBACK_CURRENCY_CODES];

  return Array.from(new Set(codes.map((code) => normalizeCurrencyCode(code)).filter(Boolean))).sort();
}

export function getCurrencyOptions(): CurrencyOption[] {
  return getAvailableCurrencyCodes().map((code) => ({
    code,
    label: getCurrencyName(code),
    symbol: getCurrencySymbol(code),
  }));
}

export function getCurrencyName(currencyCode: CurrencyCode): string {
  try {
    const DisplayNames = (Intl as IntlWithDisplayNames).DisplayNames;
    if (!DisplayNames) {
      return FALLBACK_CURRENCY_NAMES[currencyCode] ?? currencyCode;
    }

    const displayNames = new DisplayNames(undefined, { type: 'currency' });
    return displayNames?.of(currencyCode) ?? FALLBACK_CURRENCY_NAMES[currencyCode] ?? currencyCode;
  } catch {
    return FALLBACK_CURRENCY_NAMES[currencyCode] ?? currencyCode;
  }
}

export function createCurrencySearchIndex(options: CurrencyOption[]): CurrencySearchOption[] {
  return options.map((option) => ({
    ...option,
    searchText: getCurrencySearchText(option),
  }));
}

export function filterCurrencySearchOptions(
  options: CurrencySearchOption[],
  query: string,
): CurrencySearchOption[] {
  const rawQuery = query.trim().toLowerCase();
  const normalizedQuery = normalizeSearchText(query);
  if (!rawQuery) {
    return options;
  }

  return options.filter((option) => {
    if (option.symbol.toLowerCase().includes(rawQuery)) {
      return true;
    }

    return normalizedQuery ? option.searchText.includes(normalizedQuery) : false;
  });
}

export function getCurrencySearchText(option: CurrencyOption): string {
  return normalizeSearchText(
    [
      option.code,
      option.label,
      option.symbol,
      FALLBACK_CURRENCY_NAMES[option.code],
      ...(CURRENCY_SEARCH_ALIASES[option.code] ?? []),
    ]
      .filter(Boolean)
      .join(' '),
  );
}

export function normalizeSearchText(value: string): string {
  return value
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, ' ')
    .trim();
}

export function getDefaultEnabledCurrencyCodes(defaultCurrencyCode: CurrencyCode): CurrencyCode[] {
  return uniqueCurrencyCodes([defaultCurrencyCode, 'USD']);
}

export function uniqueCurrencyCodes(currencyCodes: (string | null | undefined)[]): CurrencyCode[] {
  return Array.from(
    new Set(currencyCodes.map((currencyCode) => normalizeCurrencyCode(currencyCode, '')).filter(Boolean)),
  ).sort();
}
